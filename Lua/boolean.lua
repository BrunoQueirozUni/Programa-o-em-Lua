-- Exemplo 1: Comparação de igualdade
local a = 10
local b = 5
local resultado1 = (a == b) -- false
print(resultado1)

-- Exemplo 2: Comparação de desigualdade
local x = 7
local y = 7
local resultado2 = (x ~= y) -- false
print(resultado2)

-- Exemplo 3: Operador lógico AND
local p = true
local q = false
local resultado3 = (p and q) -- false
print(resultado3)

-- Exemplo 4: Operador lógico OR
local m = true
local n = false
local resultado4 = (m or n) -- true
print(resultado4)
