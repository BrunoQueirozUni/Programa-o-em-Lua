
-- Função que retorna a soma de dois números
function soma(a, b)
   return a + b
end

-- Função que verifica se um número é par
function ehPar(num)
   return num % 2 == 0
end

-- Função que retorna o maior valor entre dois números
function maiorValor(a, b)
   if a > b then
      return a
   else
      return b
   end
end
